function translatePage() {
  // Translate all text nodes
  const walker = document.createTreeWalker(
    document.body,
    NodeFilter.SHOW_TEXT,
    null,
    false
  );
  
  const nodesToReplace = [];
  let node;
  
  while (node = walker.nextNode()) {
    const text = node.textContent.trim();
    if (TRANSLATIONS[text]) {
      nodesToReplace.push({ node, text: TRANSLATIONS[text] });
    } else {
      // Partial matches for longer text
      for (const [ar, en] of Object.entries(TRANSLATIONS)) {
        if (text.includes(ar)) {
          nodesToReplace.push({ 
            node, 
            text: text.replace(new RegExp(ar, 'g'), en) 
          });
          break;
        }
      }
    }
  }
  
  nodesToReplace.forEach(({ node, text }) => {
    node.textContent = text;
  });
  
  // Translate placeholders
  document.querySelectorAll('input[placeholder]').forEach(input => {
    const ph = input.getAttribute('placeholder');
    for (const [ar, en] of Object.entries(TRANSLATIONS)) {
      if (ph.includes(ar)) {
        input.setAttribute('placeholder', ph.replace(ar, en));
      }
    }
  });
  
  // Translate titles
  document.querySelectorAll('[title]').forEach(el => {
    const title = el.getAttribute('title');
    for (const [ar, en] of Object.entries(TRANSLATIONS)) {
      if (title === ar) {
        el.setAttribute('title', en);
      }
    }
  });
  
  // Translate aria-labels
  document.querySelectorAll('[aria-label]').forEach(el => {
    const label = el.getAttribute('aria-label');
    for (const [ar, en] of Object.entries(TRANSLATIONS)) {
      if (label === ar) {
        el.setAttribute('aria-label', en);
      }
    }
  });
}

// Run when DOM is ready
if (document.readyState === 'loading') {
  document.addEventListener('DOMContentLoaded', translatePage);
} else {
  translatePage();
}

// Also run after Astro page transitions
document.addEventListener('astro:page-load', translatePage);
